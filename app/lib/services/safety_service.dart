          AppNotification(
            id: '',
            userId: adminId,
            title: 'Multiple Safety Reports Alert',
            message: 'User $reportedUserId has received $reportCount safety reports in the last 30 days. Review required.',
            type: NotificationType.emergencyAlert,
            priority: NotificationPriority.high,
            isRead: false,
            isActionable: true,
            actionText: 'Review Reports',
            actionRoute: '/admin/safety?user=$reportedUserId',
            createdAt: DateTime.now(),
            data: {
              'reportedUserId': reportedUserId,
              'reportCount': reportCount,
              'type': 'multiple_reports_alert',
            },
          ),
        );
      }
    } catch (e) {
      debugPrint('Error notifying admins of multiple reports: $e');
    }
  }

