  // Get safety events for a user (their own reports)
  Stream<List<SafetyEventModel>> getUserSafetyEvents(String userId) {
    return _firestore
        .collection(_collection)
        .where('reporterId', isEqualTo: userId)
        .orderBy('createdAt', descending: true)
        .snapshots()
        .map((snapshot) {
      return snapshot.docs
          .map((doc) => SafetyEventModel.fromFirestore(doc))
          .toList();
    });
  }

  // Get all safety events (admin view)
  Stream<List<SafetyEventModel>> getAllSafetyEvents({
    SafetyEventStatus? status,
    SafetyEventSeverity? severity,
    int limit = 100,
  }) {
    Query query = _firestore
        .collection(_collection)
        .orderBy('createdAt', descending: true);

    if (status != null) {
      query = query.where('status', isEqualTo: status.name);
    }

    if (severity != null) {
      query = query.where('severity', isEqualTo: severity.name);
    }

    return query
        .limit(limit)
        .snapshots()
        .map((snapshot) {
      return snapshot.docs
          .map((doc) => SafetyEventModel.fromFirestore(doc))
          .toList();
    });
  }

  // Update safety event status (admin function)
  Future<bool> updateSafetyEventStatus(
    String eventId,
    SafetyEventStatus status, {
    String? reviewedBy,
    String? reviewNotes,
    String? resolution,
  }) async {
    try {
      final updateData = {
        'status': status.name,
        'updatedAt': Timestamp.fromDate(DateTime.now()),
      };

      if (reviewedBy != null) {
        updateData['reviewedBy'] = reviewedBy;
        updateData['reviewedAt'] = Timestamp.fromDate(DateTime.now());
      }

      if (reviewNotes != null) {
        updateData['reviewNotes'] = reviewNotes;
      }

      if (resolution != null) {
        updateData['resolution'] = resolution;
      }

      await _firestore.collection(_collection).doc(eventId).update(updateData);
      return true;
    } catch (e) {
      debugPrint('Error updating safety event status: $e');
      return false;
    }
  }

  // Get safety event summary/statistics
  Future<SafetyEventSummary> getSafetyEventSummary({
    DateTime? startDate,
    DateTime? endDate,
  }) async {
    try {
      Query query = _firestore.collection(_collection);

      if (startDate != null) {
        query = query.where('createdAt', isGreaterThanOrEqualTo: Timestamp.fromDate(startDate));
      }

      if (endDate != null) {
        query = query.where('createdAt', isLessThanOrEqualTo: Timestamp.fromDate(endDate));
      }

      final snapshot = await query.get();
      final events = snapshot.docs
          .map((doc) => SafetyEventModel.fromFirestore(doc))
          .toList();

      final eventsByType = <SafetyIncidentType, int>{};
      final eventsByUser = <String, int>{};
      int pendingEvents = 0;
      int resolvedEvents = 0;
      int criticalEvents = 0;

      for (final event in events) {
        // Count by incident type
        if (event.incidentType != null) {
          eventsByType[event.incidentType!] = (eventsByType[event.incidentType!] ?? 0) + 1;
        }

        // Count by user
        if (event.reportedUserId != null) {
          eventsByUser[event.reportedUserId!] = (eventsByUser[event.reportedUserId!] ?? 0) + 1;
        }

        // Count by status
        if (event.status == SafetyEventStatus.pending) pendingEvents++;
        if (event.status == SafetyEventStatus.resolved) resolvedEvents++;
        if (event.severity == SafetyEventSeverity.critical) criticalEvents++;
      }

      return SafetyEventSummary(
        totalEvents: events.length,
        pendingEvents: pendingEvents,
        resolvedEvents: resolvedEvents,
        criticalEvents: criticalEvents,
        eventsByType: eventsByType,
        eventsByUser: eventsByUser,
        lastUpdated: DateTime.now(),
      );
    } catch (e) {
      debugPrint('Error getting safety event summary: $e');
      return SafetyEventSummary(
        totalEvents: 0,
        pendingEvents: 0,
        resolvedEvents: 0,
        criticalEvents: 0,
        eventsByType: {},
        eventsByUser: {},
        lastUpdated: DateTime.now(),
      );
    }
  }

