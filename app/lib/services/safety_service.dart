  // Create a new safety event
  Future<String?> createSafetyEvent(SafetyEventModel event) async {
    try {
      // Validate and sanitize data before storing
      final eventMap = event.toMap();
      final validationErrors = SecurityUtils.validateSafetyEventData(eventMap);
      
      if (validationErrors.isNotEmpty) {
        debugPrint('Safety event validation failed: $validationErrors');
        SecurityUtils.logSecurityEvent('invalid_safety_event_data', {
          'errors': validationErrors,
          'eventType': event.eventType.name,
        });
        return null;
      }

      // Create event with sanitized data
      final docRef = await _firestore.collection(_collection).add(eventMap);
      debugPrint('Created safety event: ${docRef.id}');
      
      // Log security event for audit trail
      SecurityUtils.logSecurityEvent('safety_event_created', {
        'eventId': docRef.id,
        'eventType': event.eventType.name,
        'severity': event.severity.name,
        'reporterId': SecurityUtils.hashData(event.reporterId),
      });
      
      // Auto-notify admins for critical events
      if (event.severity == SafetyEventSeverity.critical) {
        await _notifyAdminsOfCriticalEvent(event.copyWith(id: docRef.id));
      }
      
      // Check for multiple reports against same user
      if (event.reportedUserId != null) {
        await _checkMultipleReports(event.reportedUserId!);
      }
      
      return docRef.id;
    } catch (e) {
      debugPrint('Error creating safety event: $e');
      SecurityUtils.logSecurityEvent('safety_event_creation_error', {
        'error': e.toString(),
        'eventType': event.eventType.name,
      });
      return null;
    }
  }

  // Report a safety incident from user
  Future<String?> reportSafetyIncident({
    required String reporterId,
    required SafetyIncidentType incidentType,
    required String title,
    required String description,
    String? reportedUserId,
    String? rideId,
    SafetyEventSeverity severity = SafetyEventSeverity.medium,
    List<SafetyEventEvidence> evidence = const [],
    bool isAnonymous = false,
  }) async {
    try {
      // Get current location if available
      final location = await _getCurrentLocationForEvent();
      
      final event = SafetyEventModel(
        id: '',
        eventType: SafetyEventType.userReported,
        incidentType: incidentType,
        reporterId: reporterId,
        reportedUserId: reportedUserId,
        rideId: rideId,
        title: title,
        description: description,
        severity: severity,
        status: SafetyEventStatus.pending,
        location: location,
        evidence: evidence,
        metadata: {
          'reportMethod': 'in_app_form',
          'deviceInfo': await _getDeviceInfo(),
        },
        timestamp: DateTime.now(),
        createdAt: DateTime.now(),
        updatedAt: DateTime.now(),
        tags: _generateTags(incidentType, severity),
        isAnonymous: isAnonymous,
        systemData: {
          'autoGenerated': false,
          'sourceService': 'user_report',
        },
      );

      final eventId = await createSafetyEvent(event);
      
      if (eventId != null) {
        // Send confirmation to reporter
        await _sendReportConfirmation(reporterId, eventId, isAnonymous);
      }
      
      return eventId;
    } catch (e) {
      debugPrint('Error reporting safety incident: $e');
      return null;
    }
  }

