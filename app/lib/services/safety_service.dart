  Future<void> _notifyEmergencyContacts(String userId, SafetyEventModel event) async {
    try {
      // Get user's emergency contacts from user document
      final userDoc = await _firestore.collection('users').doc(userId).get();
      
      if (userDoc.exists) {
        final userData = userDoc.data() as Map<String, dynamic>;
        final emergencyContacts = userData['emergencyContacts'] as List<dynamic>? ?? [];
        
        for (final contactData in emergencyContacts) {
          final contact = contactData as Map<String, dynamic>;
          
          // Create emergency contact notification entry for external processing
          await _createEmergencyContactNotification(
            contactData: contact,
            event: event,
            userDisplayName: userData['displayName'] ?? 'CavPool User',
          );
        }
      }
    } catch (e) {
      debugPrint('Error notifying emergency contacts: $e');
    }
  }

  Future<void> _createEmergencyContactNotification({
    required Map<String, dynamic> contactData,
    required SafetyEventModel event,
    required String userDisplayName,
  }) async {
    try {
      final locationString = _getLocationString(event.location);
      final message = '''
🚨 EMERGENCY ALERT 🚨

$userDisplayName has activated their emergency button and may need immediate assistance.

Time: ${DateTime.now().toString().split('.')[0]}
Location: $locationString
${event.rideId != null ? 'During ride: ${event.rideId}' : 'Not during active ride'}

This is an automated emergency notification from CavPool Safety System.

If this is a life-threatening emergency, call 911 immediately.
      '''.trim();

      // Store notification for external SMS/email processing
      await _firestore.collection('emergency_notifications').add({
        'contactName': contactData['name'] ?? 'Emergency Contact',
        'contactPhone': contactData['phoneNumber'] ?? '',
        'contactEmail': contactData['email'] ?? '',
        'message': message,
        'eventId': event.id,
        'userId': event.reporterId,
        'userDisplayName': userDisplayName,
        'location': locationString,
        'rideId': event.rideId,
        'priority': 'emergency',
        'status': 'pending',
        'createdAt': Timestamp.fromDate(DateTime.now()),
        'deliveryMethods': ['sms', 'email'], // Both SMS and email for redundancy
      });

      // Also send immediate notification via delivery service
      final phoneNumber = contactData['phoneNumber'] ?? '';
      final email = contactData['email'] ?? '';
      
      if (phoneNumber.isNotEmpty || email.isNotEmpty) {
        await _deliveryService.sendEmergencyNotification(
          phoneNumber: phoneNumber,
          email: email,
          message: message,
          emergencyType: 'emergency',
        );
      }

      debugPrint('Emergency contact notification sent to: ${contactData['name']}');
    } catch (e) {
      debugPrint('Error creating emergency contact notification: $e');
    }
  }

