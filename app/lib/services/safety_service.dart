  Future<void> _handleEmergencyResponse(SafetyEventModel event) async {
    debugPrint('EMERGENCY BUTTON ACTIVATED: ${event.id}');
    
    try {
      // 1. Immediate admin notification
      await _notifyAdminsOfEmergency(event);
      
      // 2. Get user's emergency contacts and notify them
      if (event.reporterId.isNotEmpty) {
        await _notifyEmergencyContacts(event.reporterId, event);
      }
      
      // 3. Start enhanced location tracking if ride is active
      if (event.rideId != null) {
        await _activateEmergencyLocationTracking(event.rideId!, event.reporterId);
      }
      
      // 4. Create emergency timeline entry
      await _logEmergencyTimeline(event.id, 'emergency_activated', {
        'activatedBy': event.reporterId,
        'rideId': event.rideId,
        'location': event.location?.coordinates != null 
            ? '${event.location!.coordinates!.latitude},${event.location!.coordinates!.longitude}'
            : 'LOCATION_UNAVAILABLE',
        'timestamp': DateTime.now().toIso8601String(),
      });
      
      // 5. Send push notification to user confirming activation
      await _sendEmergencyActivationConfirmation(event.reporterId, event.id);
      
      // 6. Log security event for audit trail
      SecurityUtils.logSecurityEvent('emergency_response_initiated', {
        'eventId': event.id,
        'userId': SecurityUtils.hashData(event.reporterId),
        'rideId': event.rideId,
        'hasLocation': event.location?.coordinates != null,
        'responseTimestamp': DateTime.now().toIso8601String(),
      });
      
    } catch (e) {
      debugPrint('Error in emergency response workflow: $e');
      
      // Even if the workflow fails, ensure admins are notified
      try {
        await _sendFailsafeEmergencyNotification(event.id, e.toString());
      } catch (failsafeError) {
        debugPrint('Critical: Failsafe emergency notification failed: $failsafeError');
      }
    }
  }

  Future<void> _checkMultipleReports(String reportedUserId) async {
    try {
      // Get reports against this user in the last 30 days
      final thirtyDaysAgo = DateTime.now().subtract(const Duration(days: 30));
      final recentReports = await _firestore
          .collection(_collection)
          .where('reportedUserId', isEqualTo: reportedUserId)
          .where('createdAt', isGreaterThan: Timestamp.fromDate(thirtyDaysAgo))
          .get();

      if (recentReports.docs.length >= 3) {
        // Multiple reports detected - notify admins
        debugPrint('MULTIPLE REPORTS DETECTED for user: $reportedUserId (${recentReports.docs.length} reports)');
        
        await _notifyAdminsOfMultipleReports(reportedUserId, recentReports.docs.length);
      }
    } catch (e) {
      debugPrint('Error checking multiple reports: $e');
    }
  }

  Future<void> _notifyAdminsOfMultipleReports(String reportedUserId, int reportCount) async {
    try {
      final adminIds = await _getAdminUserIds();
      
