      debugPrint('Critical failsafe notification creation failed: $e');
    }
  }

  String _getLocationString(SafetyEventLocation? location) {
    if (location?.coordinates != null) {
      final lat = location!.coordinates!.latitude.toStringAsFixed(6);
      final lng = location.coordinates!.longitude.toStringAsFixed(6);
      final accuracy = location.accuracy != null ? ' (±${location.accuracy!.toStringAsFixed(0)}m)' : '';
      return 'https://maps.google.com/?q=$lat,$lng$accuracy';
    }
    return 'Location unavailable';
  }
