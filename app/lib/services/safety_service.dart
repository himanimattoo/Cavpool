  List<String> _generateTags(SafetyIncidentType incidentType, SafetyEventSeverity severity) {
    final tags = <String>[incidentType.name, severity.name];
    
    if (severity == SafetyEventSeverity.critical) {
      tags.add('urgent');
    }
    
    return tags;
  }

